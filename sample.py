user_input = int(input("Enter a number under 8\n"))
if user_input == 1:
    print("sunday")
elif user_input == 2:
    print("monday")
elif user_input == 3:
    print("tuesday")
elif user_input == 4:
    print("wednesday")
elif user_input == 5:
    print("thursday")
elif user_input == 6:
    print("friday")
elif user_input == 7:
    print("saturday")
else: 
    print("invalid input")